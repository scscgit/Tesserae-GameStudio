package sk.tuke.gamestudio.game.tesserae.core.tile;

import sk.tuke.gamestudio.game.tesserae.core.TesseraeRuntimeException;

/**
 * Problem that happens when a Tile tries to move to a wrong position.
 * <p/>
 * Created by Steve on 29.2.2016.
 */
public class TileCannotMoveException extends TesseraeRuntimeException
{
	/**
	 * Constructs a new runtime exception with {@code null} as its
	 * detail message.  The cause is not initialized, and may subsequently be
	 * initialized by a call to {@link #initCause}.
	 */
	public TileCannotMoveException()
	{
		super("Tile cannot move to a requested direction.");
	}
	/**
	 * Constructs a new runtime exception with the specified detail message.
	 * The cause is not initialized, and may subsequently be initialized by a
	 * call to {@link #initCause}.
	 *
	 * @param message
	 * 	the detail message. The detail message is saved for
	 * 	later retrieval by the {@link #getMessage()} method.
	 */
	public TileCannotMoveException(String message)
	{
		super(message);
	}
}
